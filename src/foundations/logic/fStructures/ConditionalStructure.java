package foundations.logic.fStructures;

public class ConditionalStructure {
    public static void main(String[] args) {

        int bread = 10;
        int sugar = 15;


        System.out.println(bread == sugar);
        System.out.println(bread != sugar);
        System.out.println(bread > sugar);
        System.out.println(bread < sugar);
        System.out.println(bread >= sugar);
        System.out.println(bread <= sugar);


    }
}
